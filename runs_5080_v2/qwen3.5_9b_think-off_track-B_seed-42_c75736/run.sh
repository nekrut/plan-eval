# Define paths relative to current working directory
RAW_DIR="data/raw"
REF_DIR="data/ref"
RESULTS_DIR="results"

# Create results directory if it doesn't exist
mkdir -p "$RESULTS_DIR"

# Define samples
SAMPLES=("M117-bl" "M117-ch" "M117C1-bl" "M117C1-ch")

# Function to process a single sample
process_sample() {
    local sample="$1"
    local fq1="${RAW_DIR}/${sample}_1.fq.gz"
    local fq2="${RAW_DIR}/${sample}_2.fq.gz"
    local bam_out="${RESULTS_DIR}/${sample}.bam"
    local bai_out="${RESULTS_DIR}/${sample}.bam.bai"
    local vcf_out="${RESULTS_DIR}/${sample}.vcf.gz"
    local tbi_out="${RESULTS_DIR}/${sample}.vcf.gz.tbi"

    # Check if results already exist (idempotency)
    if [[ -f "$bam_out" ]] && [[ -f "$bai_out" ]] && [[ -f "$vcf_out" ]] && [[ -f "$tbi_out" ]]; then
        return 0
    fi

    # 1. Align reads to mitochondrial reference
    bwa mem -t 4 -R "@RG\tID:${sample}\tSM:${sample}\tPL:MiSeq\tPN:Miseq_v3\tPU:${sample}" \
        "${REF_DIR}/chrM.fa" "$fq1" "$fq2" 2>/dev/null | \
        samtools view -b - > "$bam_out"

    # 2. Sort and index BAM
    samtools sort -o "${bam_out}.sorted" "$bam_out"
    samtools index "${bam_out}.sorted"
    mv "${bam_out}.sorted" "$bam_out"

    # 3. Call variants using LoFreq (suitable for low-frequency variants in amplicons)
    lofreq call \
        --pooled 0 \
        --min-coverage 1 \
        --min-qual 20 \
        --min-freq 0.01 \
